package Basic.If_Else;

public class elseif {
    public static void main(String[] args) {
        boolean tipo = true;

        if(tipo == true){
            System.out.println("Verdadeiro");
        }else if(tipo == false){
            System.out.println("Falso");
        }else{
            System.out.println("Batata");
        }
    }
    
}

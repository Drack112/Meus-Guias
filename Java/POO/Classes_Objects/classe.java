package POO.Classes_Objects;

// Começar uma classe de objetos
public class classe {
    // Declarar a variável
    int x = 5;

    public static void main(String[] args) {
        // Criamos o objeto
        classe myOBJ = new classe();

        // Passamos o valor dele
        System.out.println(myOBJ.x);
    }
}
